#include "stringbad.h"
#include <cstring>
using std::cout;


int StringBad::num_strings = 0;

StringBad::StringBad(const char* s)
{
	len = std::strlen(s);
	str = new char(len + 1);
	std::strcpy(str, s);
	num_strings++;
	cout << num_strings << ":\"" << str << "\"object created\n";
}

StringBad::StringBad()
{
	len = 4;
	str = new char[4];
	std::strcpy(str, "C++");
	num_strings++;
	cout << num_strings << ":\"" << str << "\"default object created\n";
}
//显示的定义默认复制构造函数，并进行深度复制
StringBad::StringBad(const StringBad& st)
{
	num_strings++;
	len = st.len;
	str = new char[len + 1];
	std::strcpy(str, st.str);
	cout << num_strings << ":\"" << str << "\" object created\n";
}
//深度复制 
StringBad& StringBad::operator=(const StringBad& st)
{
	if (this == &st)
		return *this;
	delete[] str; //释放str原先指向的内存，以便将一个新字符串的地址赋给str
	len = st.len;
	str = new char[len + 1];
	std::strcpy(str, st.str);
	return *this;
	// TODO: 在此处插入 return 语句
}

StringBad::~StringBad()
{
	cout << "\"" << str << "\" object deleted,";
	--num_strings;
	cout << num_strings << " left\n";
	delete[] str;
}

